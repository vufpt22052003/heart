    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String a = TXTNAME.getText();
        if (a.equals("")) {
            TXTNAME.setBackground(Color.red);
            JOptionPane.showConfirmDialog(this, a + " Tên không được để trống");
            return;
        } else {
            TXTNAME.setBackground(Color.white);
        }
        String b = TXTDIEM.getText();
        if (b.equals("")) {
            TXTDIEM.setBackground(Color.red);
            JOptionPane.showConfirmDialog(this, b + " Mật khẩu Không được để trống ");
            return;
        } else {
            TXTDIEM.setBackground(Color.white);
        }

        this.addStuden();
        this.fillToTable();
    }//GEN-LAST:event_jButton1ActionPerformed

