    public void fillToTable() {
        // xóa các côt
        tbModel.setRowCount(0);
        // dùng for duyẹt mảng
        for (studen sv : list) {
            tbModel.addRow(new Object[]{sv.name, sv.major, sv.marks});
        }
        tbModel.fireTableDataChanged();
    }

    public void showDetail() {
        int index;
        index = studen.getSelectedRow();
        studen sv = list.get(index);
        sv.name = TXTNAME.getText();
        sv.marks = Double.parseDouble(TXTDIEM.getText());
        sv.major = (String)ng.getSelectedItem();         
    }
    

