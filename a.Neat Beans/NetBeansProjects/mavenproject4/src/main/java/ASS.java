    public void save() {
        // Không để trống mã, không cho phép trùng mã nếu bổ sung mới.
        // Tuổi phải từ 16 đến 55
        // Lương phải trên 5 triệu
        //Email phải đúng định dạng
        String check1 = TXTNV.getText();
        String check2 = TXTHVT.getText();
        String check3 = TXTage.getText();
        String check4 = TXTemai.getText();
        String check5 = TXTL.getText();

        if (check1.equals("")) {
            JOptionPane.showConfirmDialog(this, check1 + "Xin điền thông tin");
            TXTNV.setBackground(Color.red);
            return;
        } else {
            TXTNV.setBackground(Color.white);
        }
        if (check2.equals("")) {
            JOptionPane.showConfirmDialog(this, check2 + "Xin điền thông tin");
            TXTHVT.setBackground(Color.red);
            return;
        } else {
            TXTHVT.setBackground(Color.white);
        }
        if (check3.equals("")) {

            JOptionPane.showConfirmDialog(this, check3 + "Xin điền thông tin");
            TXTage.setBackground(Color.red);
            return;
        } else {
            TXTage.setBackground(Color.white);
        }
        if (check4.equals("")) {
            JOptionPane.showConfirmDialog(this, check4 + "Xin điền thông tin");
            TXTemai.setBackground(Color.red);
            return;
        } else {
            TXTemai.setBackground(Color.white);
        }
        // kiểm tra định dạng email
        Pattern p = Pattern.compile("^[A-Za-z0-9]+\\@[A-Za-z0-9]+\\.[A-Za-z0-9]+$");
        if (p.matcher(check4).find()) {
            return;
        } else {
            JOptionPane.showConfirmDialog(null, "email không đúng định dạng");

        }
        if (check5.equals("")) {
            JOptionPane.showConfirmDialog(this, check5 + "Xin điền thông tin");
            TXTL.setBackground(Color.red);
        } else {
            TXTL.setBackground(Color.white);
        }
        //Lương phải trên 5 triệu
        if (Double.parseDouble(TXTL.getText()) < 5000000) {
            JOptionPane.showConfirmDialog(null, "Lương phải lớn hơn 5 triệu");
        }
    }

