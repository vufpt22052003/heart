    public void add(Empoly emp) {
        list.add(emp);

    }

    public Empoly FindByid(String id) {
        for (Empoly empoly : list) {
            if (empoly.getId().equals(id)) {
                return empoly;
            }
        }
        return null;
    }

    public boolean deleteByid(String id) {
        for (Empoly empoly : list) {
            if (empoly.getId().equals(id)) {
                list.remove(empoly);
                return true;
            }
        }
        return false;
    }

    public boolean update(Empoly emp) {
        Empoly existeEmp = FindByid(emp.getId());
        boolean flag = false;
        if (existeEmp != null) {
            existeEmp.setName(emp.getName());
            existeEmp.setAge(emp.getAge());
            existeEmp.setEmail(emp.getEmail());
            existeEmp.setLuong(emp.getLuong());
            flag = true;
        }
        return flag;

    }

    public void initTable() {
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new Object[]{"Mã", "Họ và tên", "Tuổi", "Email", "Lương"});

    }

    public void rederToTable(DefaultTableModel tbModel) {
        tbModel.setRowCount(0);
        for (Empoly empoly : list) {
            Object[] row = new Object[]{
                empoly.getId(), empoly.getName(), empoly.getAge(),
                empoly.getEmail(), empoly.getLuong()
            };
            tbModel.addRow(row);
        }
        tblModel.fireTableDataChanged();
    }

