    private void btnBangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBangActionPerformed
        double pt1 = Double.parseDouble(this.pt1);
        double pt2 = Double.parseDouble(txtSo.getText());
        if (pt.equals("+")) {
            result = pt1 + pt2;
        } else if (pt.equals("-")) {
            result = pt1 - pt2;
        }
        if (pt.equals("*")) {
            result = pt1 * pt2;
        }
        if (pt.equals("/")) {
            result = pt1 / pt2;
        }
        txtSo.setText(" " + result);
    }//GEN-LAST:event_btnBangActionPerformed

